import UIKit

extension UIColor {
    @nonobjc class var briliantYellow: UIColor {
        return UIColor(red: 231.0 / 255.0, green: 231.0 / 255.0, blue: 80.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var neutralGray: UIColor {
        return UIColor(red: 137.0 / 255.0, green: 142.0 / 255.0, blue: 140.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var sadBlue: UIColor {
        return UIColor(red: 64.0 / 255.0, green: 80.0 / 255.0, blue: 116.0 / 255.0, alpha: 1.0)
    }
}
